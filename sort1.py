# БЛОЧНАЯ СОРТИРОВКА (BUCKET SORT)
def f1(a):
    """
    Алгоритм блочной сортировки для чисел в диапазоне [0,1)
    Распределяет элементы по корзинам, сортирует корзины, объединяет результат
    """
    if not a:
        return a
    
    n = len(a)
    b = [[] for _ in range(n)]
    
    # Распределение по корзинам
    for x in a:
        i = int(x * n)
        b[i].append(x)
    
    # Сортировка каждой корзины
    for c in b:
        c.sort()
    
    # Объединение результатов
    d = []
    for c in b:
        d.extend(c)
    
    return d

# Пример использования
if __name__ == "__main__":
    a = [0.42, 0.32, 0.33, 0.52, 0.37, 0.47, 0.51]
    print("Блочная сортировка:")
    print("Вход:", a)
    print("Выход:", f1(a))